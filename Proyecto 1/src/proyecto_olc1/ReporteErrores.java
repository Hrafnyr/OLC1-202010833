    public void addNewRow(){
        
        for (int i = 0; i < Analizador_Lexico.TError.size(); i++) {
            //obtener el objeto
            claseErrores obj = ((claseErrores)Analizador_Lexico.TError.get(i));
            
            String vector[]={obj.getLexema(),obj.getTipo(),obj.getDescripcion(),String.valueOf(obj.getFila()),String.valueOf(obj.getColumna())};
            DefaultTableModel tb = (DefaultTableModel)tablaErrores.getModel();
            tb.addRow(vector);
                 
        }
          
    }
    
    public void Clear_T(){
        for (int i = 0; i < tablaErrores.getRowCount(); i++) {
            DefaultTableModel tb = (DefaultTableModel)tablaErrores.getModel();
            tb.removeRow(i);
            i-=1;
        }
    }
    
    
